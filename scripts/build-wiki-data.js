/**
 * Сборочный шаг для базы знаний.
 *
 * Читает все .md-файлы из content/wiki/{section}/, парсит body через marked
 * и пишет lib/wiki-articles.generated.ts — статический ts-файл, который дальше
 * импортируется и серверными, и клиентскими компонентами Next.
 *
 * Запускается автоматически перед `npm run dev` и `npm run build`
 * через хуки predev/prebuild в package.json.
 */

const fs = require("fs");
const path = require("path");
const matter = require("gray-matter");
const { marked } = require("marked");

const ROOT = path.resolve(__dirname, "..");
const CONTENT_ROOT = path.join(ROOT, "content", "wiki");
const OUT_PATH = path.join(ROOT, "lib", "wiki-articles.generated.ts");
const SECTIONS = ["function", "custom", "tech"];

marked.use({ gfm: true, breaks: false });

const CALLOUT_RE = /^\[!CALLOUT(?:\s+color=([^\]\s]+))?\]\s*\n?([\s\S]*)$/;
const PREFACE_RE = /^\[!PREFACE\]\s*\n?([\s\S]*)$/;
const DB_SCHEMA_RE = /^:::db-schema:::$/;
const VIDEO_RE = /^:::video\[([^\]]+)\]\{([^}]+)\}$/;

function inlineHtml(md) {
  return marked.parseInline(md, { async: false });
}

function blockHtml(token) {
  return marked.parser([token]);
}

function blockHtmlFromMd(md) {
  return marked.parse(md, { async: false });
}

function parseBody(md) {
  const tokens = marked.lexer(md);
  const blocks = [];

  for (let i = 0; i < tokens.length; i += 1) {
    const tok = tokens[i];
    if (tok.type === "space") continue;

    if (tok.type === "heading") {
      blocks.push({ ty: "heading", te: inlineHtml(tok.text), le: tok.depth });
      continue;
    }

    if (tok.type === "paragraph") {
      const text = (tok.text || "").trim();

      if (DB_SCHEMA_RE.test(text)) {
        blocks.push({ ty: "db-schema" });
        continue;
      }
      const v = text.match(VIDEO_RE);
      if (v) {
        blocks.push({ ty: "video", p: v[1], url: v[2] });
        continue;
      }

      const inline = tok.tokens || [];
      if (inline.length === 1 && inline[0].type === "image") {
        const img = inline[0];
        let caption = "";
        const next = tokens[i + 1];
        if (
          next &&
          next.type === "paragraph" &&
          next.tokens &&
          next.tokens.length === 1 &&
          next.tokens[0].type === "em"
        ) {
          caption = next.tokens[0].text || "";
          i += 1;
        }
        blocks.push({
          ty: "image",
          url: img.href,
          alt: img.text || "",
          caption,
        });
        continue;
      }

      blocks.push({ ty: "text", te: inlineHtml(tok.text) });
      continue;
    }

    if (tok.type === "list") {
      blocks.push({ ty: "text", te: blockHtml(tok) });
      continue;
    }

    if (tok.type === "blockquote") {
      const raw = (tok.text || "").trim();

      const callout = raw.match(CALLOUT_RE);
      if (callout) {
        blocks.push({
          ty: "callout",
          te: blockHtmlFromMd(callout[2] || ""),
          ic: callout[1] || "",
        });
        continue;
      }

      const preface = raw.match(PREFACE_RE);
      if (preface) {
        blocks.push({ ty: "preface", te: inlineHtml(preface[1] || "") });
        continue;
      }

      blocks.push({ ty: "text", te: blockHtml(tok) });
      continue;
    }

    if (tok.type === "hr") {
      blocks.push({ ty: "br" });
      continue;
    }

    if (tok.type === "html") {
      blocks.push({ ty: "text", te: tok.text });
      continue;
    }

    if (tok.type === "table" || tok.type === "code") {
      blocks.push({ ty: "text", te: blockHtml(tok) });
      continue;
    }
  }

  return blocks;
}

function readSection(section) {
  const dir = path.join(CONTENT_ROOT, section);
  if (!fs.existsSync(dir)) return [];
  const files = fs.readdirSync(dir).filter((f) => f.endsWith(".md")).sort();

  return files.map((file) => {
    const slug = file.replace(/\.md$/, "");
    const raw = fs.readFileSync(path.join(dir, file), "utf-8");
    const { data, content } = matter(raw);
    return {
      id: data.id || slug,
      title: data.title || slug,
      category: data.category || "Без категории",
      image: data.cover || "",
      slug,
      text: parseBody(content),
    };
  });
}

function main() {
  const data = {};
  let total = 0;
  for (const section of SECTIONS) {
    const articles = readSection(section);
    data[section] = articles;
    total += articles.length;
  }

  const header = `// AUTO-GENERATED by scripts/build-wiki-data.js — do not edit.\n// Source of truth: content/wiki/**/*.md\n\nexport interface WikiSourceArticle {\n  id: string;\n  title: string;\n  category: string;\n  image: string;\n  slug: string;\n  text: any[];\n}\n\n`;

  const body = SECTIONS.map((section) => {
    const varName = `wiki${section[0].toUpperCase() + section.slice(1)}Articles`;
    return `export const ${varName}: WikiSourceArticle[] = ${JSON.stringify(data[section], null, 2)};`;
  }).join("\n\n");

  const tail = `\n\nexport type WikiFunctionArticle = WikiSourceArticle;\nexport type WikiCustomArticle = WikiSourceArticle;\nexport type WikiTechArticle = WikiSourceArticle;\n`;

  fs.writeFileSync(OUT_PATH, header + body + tail, "utf-8");
  console.log(`wiki-articles.generated.ts: ${total} articles across ${SECTIONS.length} sections`);
}

main();
